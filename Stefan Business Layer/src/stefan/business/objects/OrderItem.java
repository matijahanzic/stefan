package stefan.business.objects;

/**
 *
 * @author Matija
 */
public class OrderItem {     
   
    private Integer idOrderItems;  
    private int designId;
    private String position;    
    private Integer quantityOrdered;   
    private Integer quantityDelivered;   
    private Order order;    
    private Design design;

    /**
     * @return the idOrderItems
     */
    public Integer getIdOrderItems() {
        return idOrderItems;
    }

    /**
     * @param idOrderItems the idOrderItems to set
     */
    public void setIdOrderItems(Integer idOrderItems) {
        this.idOrderItems = idOrderItems;
    }

    /**
     * @return the position
     */
    public String getPosition() {
        return position;
    }

    /**
     * @param position the position to set
     */
    public void setPosition(String position) {
        this.position = position;
    }

    /**
     * @return the quantityOrdered
     */
    public Integer getQuantityOrdered() {
        return quantityOrdered;
    }

    /**
     * @param quantityOrdered the quantityOrdered to set
     */
    public void setQuantityOrdered(Integer quantityOrdered) {
        this.quantityOrdered = quantityOrdered;
    }

    /**
     * @return the quantityDelivered
     */
    public Integer getQuantityDelivered() {
        return quantityDelivered;
    }

    /**
     * @param quantityDelivered the quantityDelivered to set
     */
    public void setQuantityDelivered(Integer quantityDelivered) {
        this.quantityDelivered = quantityDelivered;
    }

    /**
     * @return the order
     */
    public Order getOrder() {
        return order;
    }

    /**
     * @param order the order to set
     */
    public void setOrder(Order order) {
        this.order = order;
    }

    /**
     * @return the design
     */
    public Design getDesign() {
        return design;
    }

    /**
     * @param design the design to set
     */
    public void setDesign(Design design) {
        this.design = design;
    }

    /**
     * @return the designId
     */
    public int getDesignId() {
        return designId;
    }

    /**
     * @param designId the designId to set
     */
    public void setDesignId(int designId) {
        this.designId = designId;
    }
}
